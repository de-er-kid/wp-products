function get_store_currency_sign() {
  $currency_code = get_option('wpecom_currency');
  if (empty($currency_code)) {
      $currency_code = 'USD';
  }
  $currency_code = sanitize_text_field($currency_code);
  $currency_symbol = get_currency_symbol($currency_code);
  $currency_symbol = sanitize_text_field($currency_symbol);
  return $currency_symbol;
}

global $store_currency_sign;
if ( function_exists( 'get_option' ) ) {
    $store_currency_sign = get_store_currency_sign();
} else {
    $store_currency_sign = '$';
}
